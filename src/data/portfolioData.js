const normalizeCloudinaryAsset = (url) => {
  const cleaned = url.trim().replace(/^=+\s*/, "");

  if (!cleaned) {
    return null;
  }

  // Portfolio gallery uses <img>, so video links are converted to first-frame JPGs.
  if (cleaned.includes("/video/upload/") && cleaned.endsWith(".mp4")) {
    return cleaned
      .replace("/video/upload/", "/video/upload/so_0/")
      .replace(/\.mp4$/, ".jpg");
  }

  return cleaned;
};

